            <h2 class="text-base font-bold text-[#333333] pt-4">How Much Will You Save on Your Bill?</h2>

            <p>Typical savings run between ₱1,500 and ₱6,000 a month depending on your system size and how you use electricity. If you're running aircon during the day when the panels are generating, you'll feel the difference a lot more.</p>
            <p>For a household with a ₱5,500 monthly bill, a well-sized system can wipe out almost all the variable charges, leaving just fixed metering fees of around ₱200. That's roughly a 96% reduction.</p>
            <p>The mechanism behind this is <strong class="text-[#333333]">net metering</strong>: excess power your panels produce gets sent to the grid, and Meralco credits that against your next bill. You use solar during the day, export the surplus, and draw from the grid at night with the credits covering most of it.</p>

