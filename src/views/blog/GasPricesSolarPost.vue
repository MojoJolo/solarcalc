            <h2 class="text-base font-bold text-[#333333] pt-4">Solar Helps You Lock In Part of Your Energy Cost</h2>

            <p>That is the real appeal. Solar does not just lower your bill today. It also reduces how exposed you are to future rate increases.</p>
            <p>Once your system is installed, the sunlight hitting your roof does not suddenly get more expensive because oil or fuel markets moved. Your upfront system cost stays the same, but the value of the electricity you produce can rise when grid prices go up.</p>

            <h2 class="text-base font-bold text-[#333333] pt-4">Why This Improves ROI</h2>

            <p>Solar ROI depends heavily on one thing: how expensive grid electricity is versus how much of it your system replaces. If utility rates climb, every kilowatt-hour your panels generate offsets more pesos.</p>
            <p>That means higher fuel-driven power costs can shorten your payback period. The same solar system can become a better financial decision simply because the alternative, staying fully dependent on the grid, got more expensive.</p>

