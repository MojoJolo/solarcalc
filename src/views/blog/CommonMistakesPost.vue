        <div class="space-y-5 text-sm text-gray-600 leading-relaxed">
            <p>Solar is one of the few home upgrades in the Philippines that can actually pay for itself. But plenty of homeowners still end up with systems that are overpriced, undersized, or badly installed because they rushed the buying process.</p>
            <p>If you're comparing quotes right now, these are the mistakes worth avoiding.</p>

            <h2 class="text-base font-bold text-[#333333] pt-4">1. Choosing the Cheapest Quote Too Fast</h2>

            <p>A low quote can look attractive until you find out what was removed to get there: lower-grade mounting hardware, weaker after-sales support, unclear warranties, or corners cut during installation.</p>
            <p>Ask what is actually included. You want panel brand, inverter brand, mounting system, permits, net metering support, monitoring app setup, and warranty coverage spelled out in writing.</p>

