            <h2 class="text-base font-bold text-[#333333] pt-4">4. Ignoring Roof Conditions</h2>

            <p>Not every roof is equally easy or cheap to work with. Tiled roofs usually need more labor. Older roofs may need reinforcement or repairs before installation. Shading from trees, neighboring houses, or water tanks can cut production more than people expect.</p>
            <p>Ask for a proper site assessment, not just a messenger quote based on your address and monthly bill.</p>

            <h2 class="text-base font-bold text-[#333333] pt-4">5. Assuming Net Metering Is Automatic</h2>

            <p>Many homeowners think solar savings kick in the moment the system is switched on. In practice, net metering is a separate process. Your installer should help with paperwork, utility coordination, and meter replacement if needed.</p>
            <p>If this part is not included or explained clearly, ask before you sign. A good installer should be able to tell you the process, timeline, and expected fees.</p>

            <h2 class="text-base font-bold text-[#333333] pt-4">6. Not Asking About After-Sales Support</h2>

            <p>Solar is not a one-day purchase. You are also buying years of support. If the monitoring app stops working, output drops, or a component fails, you need someone who actually answers.</p>
            <ul class="space-y-3 pl-4">
                <li class="flex gap-2"><span class="text-[#FFD93D] font-bold mt-0.5">·</span><span><strong class="text-[#333333]">Ask about workmanship warranty.</strong> Product warranties are not enough if installation issues are excluded.</span></li>
                <li class="flex gap-2"><span class="text-[#FFD93D] font-bold mt-0.5">·</span><span><strong class="text-[#333333]">Ask who handles warranty claims.</strong> You want the installer to coordinate this, not leave you chasing manufacturers yourself.</span></li>
                <li class="flex gap-2"><span class="text-[#FFD93D] font-bold mt-0.5">·</span><span><strong class="text-[#333333]">Ask what happens after turnover.</strong> Monitoring, checkups, and response time matter.</span></li>
            </ul>

            <h2 class="text-base font-bold text-[#333333] pt-4">7. Financing Without Checking the Math</h2>

            <p>Installment plans can make solar easier to buy, but only if the monthly payment still makes sense against your expected bill savings. If the financing cost wipes out the short-term savings, that changes the decision.</p>
            <p>Run the numbers using your actual system quote. Look at monthly amortization, annual savings, and payback period together, not separately.</p>

            <h2 class="text-base font-bold text-[#333333] pt-4">What a Good Solar Buying Process Looks Like</h2>

            <p>Get at least three detailed quotes. Compare scope, not just sticker price. Ask about net metering, warranties, timeline, and support. Make sure the proposed system matches your real bill and your roof condition.</p>
            <p>That sounds basic, but it eliminates most expensive mistakes.</p>

            <h2 class="text-base font-bold text-[#333333] pt-4">Start With Your Own Numbers</h2>

            <p>Before talking to installers, figure out roughly how many panels you need and what kind of savings you should expect. That gives you a baseline and makes it much easier to spot a weak quote.</p>

