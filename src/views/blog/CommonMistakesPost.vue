            <h2 class="text-base font-bold text-[#333333] pt-4">2. Focusing Only on Panel Brand</h2>

            <p>A lot of buyers obsess over whether the panels are Jinko, Longi, Trina, or Canadian Solar. In reality, a bad installer using a good panel brand is still a bad deal.</p>
            <p>The inverter, wiring, roof penetrations, layout, and workmanship matter just as much. A clean design and a reliable installer usually make a bigger difference than chasing one specific panel label.</p>

            <h2 class="text-base font-bold text-[#333333] pt-4">3. Buying a System That Doesn't Match Your Actual Bill</h2>

            <p>Some homeowners buy based on roof space alone. Others buy based on what fits their budget, even if the system size barely dents the bill. Both approaches can miss the point.</p>
            <p>Start with your real electricity usage. Your monthly bill tells you far more about the right system size than your roof photo does. Once you know your target savings, you can size the system properly and avoid overbuying or underbuying.</p>

